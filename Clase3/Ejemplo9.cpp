#include <iostream>
using namespace std;
#include<cmath> //contiene las funciones matemáticas

int main () {
	double pi = M_PI; //M_PI contiene pi
	cout<<"Valor de pi: "<<pi<<endl;
	cout<<"Raiz cuadrada de pi: "<<sqrt(pi)<<endl;
	cout<<"Algunas funciones trigonometricas:"<<endl;
	cout<<"sen(pi/3) = "<<sin(pi/3)<<endl;
	cout<<"cos(pi/3) = "<<cos(pi/3)<<endl;
	cout<<"tan(pi/3) = "<<tan(pi/3)<<endl;
	return 0;
}
