#include<iostream> 
using namespace std; 
  
int main() 
{ 
    cout << "Tamaño de  char : " << sizeof(char)  
      << " byte" << endl; 
    cout << "Tamaño de  int : " << sizeof(int) 
      << " bytes" << endl; 
    cout << "Tamaño de  short int : " << sizeof(short int)  
      << " bytes" << endl; 
    cout << "Tamaño de  long int : " << sizeof(long int)  
       << " bytes" << endl; 
    cout << "Tamaño de  signed long int : " << sizeof(signed long int) 
       << " bytes" << endl; 
    cout << "Tamaño de  unsigned long int : " << sizeof(unsigned long int)  
       << " bytes" << endl; 
    cout << "Tamaño de  float : " << sizeof(float)  
       << " bytes" <<endl; 
    cout << "Tamaño de  double : " << sizeof(double)  
       << " bytes" << endl; 
    cout << "Tamaño de  wchar_t : " << sizeof(wchar_t)  
       << " bytes" <<endl; 
      
    return 0; 
} 
